import sqlite3
from Global import CONFIG


class CSqliteOpt():
    """
    Sqlite3 简单操作函数
    """

    def __init__(self):
        """
        初始化数据库，默认文件名 stsql.db
        filename：文件名
        """
        self.filename = CONFIG["@Sqlite3"]["db_file"]
        self.db = sqlite3.connect(self.filename)
        self.c = self.db.cursor()

    def __del__(self):
        self.close()

    def close(self):
        """
        关闭数据库
        """
        self.c.close()
        self.db.close()

    def execute(self, sql, param=None):
        """
        执行数据库的增、删、改
        sql：sql语句
        param：数据，可以是list或tuple，亦可是None
        retutn：成功返回True
        """
        try:
            if param is None:
                self.c.execute(sql)
            else:
                if type(param) is list:
                    self.c.executemany(sql, param)
                else:
                    self.c.execute(sql, param)
            count = self.db.total_changes
            self.db.commit()
        except Exception as e:
            print(e)
            return False
        if count > 0:
            return True
        else:
            return False

    def query(self, sql, param=None):
        """
        查询语句
        sql：sql语句
        param：参数,可为None
        retutn：成功返回True
        """
        if param is None:
            self.c.execute(sql)
        else:
            self.c.execute(sql, param)
        return self.c.fetchall()
